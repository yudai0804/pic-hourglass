    GOTO _STATE6
    ; STATE6終了
    MOVLW CNT3_DEFAULT
    MOVWF CNT3
    MOVLW CNT5_DEFAULT
    MOVWF CNT5
STATE7
    ; state7は消灯のみ
    DECFSZ CNT3
    GOTO _STATE7
    ; STATE7終了
    GOTO LOOP

_STATE0
