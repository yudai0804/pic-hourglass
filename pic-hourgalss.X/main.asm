LIST P=PIC16F1827
#include<p16f1827.inc>

CNT0	EQU	0x70
CNT1	EQU	0x71
CNT2	EQU	0x72
CNT3 EQU 0x73
; TODO: CNT3_DEFAULTを廃止してメモリからデータを読むようにする
;20.825
CNT3_1MIN EQU d'22'
;62.5
CNT3_3MIN EQU d'64'
;104.1666...
CNT3_5MIN EQU d'105'
CNT4 EQU 0x74
CNT4_DEFAULT EQU d'80'
CNT5 EQU 0x75
CNT5_DEFAULT EQU d'40'
CNT5_HALF EQU d'20'
 
 CNT3_MEM EQU 0x76
 
 START_SW EQU d'4'
 RESET_SW EQU d'5'

__CONFIG	_CONFIG1, _FOSC_INTOSC & _WDTE_OFF & _PWRTE_ON & _MCLRE_OFF & _CP_OFF & _CPD_OFF & _BOREN_OFF & _CLKOUTEN_OFF & _IESO_OFF & _FCMEN_OFF
__CONFIG	_CONFIG2, _WRT_OFF & _PLLEN_OFF & _STVREN_OFF & _BORV_LO & _LVP_OFF

ORG 0x0000
    CLRF PORTA
    CLRF PORTB
    BSF BSR, BSR0
    ; バンク1
    ; クロックを4MHzに設定
    MOVLW B'01101000'
    MOVWF OSCCON
    BSF BSR, BSR1
    ; バンク3
    ; ディジタル入力を受け入れるようにする
    ; CLRF ANSELAをすると何故かRA4の動きがXORみたいな動きをするようになる。
    ; 今回はANSELAを行わないことで対応
    CLRF ANSELB
    ; バンク1
    BSF BSR, BSR0
    BCF BSR, BSR1
    MOVLW 0x00
    MOVWF TRISA
    MOVLW 0x30
    MOVWF TRISB
    ; バンク0
    BCF BSR, BSR0
    CLRF PORTA
    CLRF PORTB
LOOP
    ; 離し終わるまで待機
    CALL _SELECT1
    BTFSC PORTB, RESET_SW
    GOTO LOOP
SELECT1
    CALL _SELECT1
    BTFSS PORTB, START_SW
    GOTO SELECT1_LABEL1
    MOVLW CNT3_1MIN
    MOVWF CNT3_MEM
    GOTO STATE0
SELECT1_LABEL1
    BTFSS PORTB, RESET_SW
    GOTO SELECT1
    ; 離し終わるまで待機
SELECT1_LABEL2
    CALL _SELECT1
    BTFSC PORTB, RESET_SW
    GOTO SELECT1_LABEL2

SELECT3
    CALL _SELECT3
    BTFSS PORTB, START_SW
    GOTO SELECT3_LABEL1
    MOVLW CNT3_3MIN
    MOVWF CNT3_MEM
    GOTO STATE0
SELECT3_LABEL1
    BTFSS PORTB, RESET_SW
    GOTO SELECT3
    ; 離し終わるまで待機
SELECT3_LABEL2
    CALL _SELECT3
    BTFSC PORTB, RESET_SW
    GOTO SELECT3_LABEL2

SELECT5
    CALL _SELECT5
    BTFSS PORTB, START_SW
    GOTO SELECT5_LABEL1
    MOVLW CNT3_5MIN
    MOVWF CNT3_MEM
    GOTO STATE0
SELECT5_LABEL1
    BTFSS PORTB, RESET_SW
    GOTO SELECT5
    ; 離し終わるまで待機
SELECT5_LABEL2
    CALL _SELECT5
    BTFSC PORTB, RESET_SW
    GOTO SELECT5_LABEL2
    GOTO LOOP

_SELECT1
    CALL LED2
    CALL LED6
    CALL LED7
    CALL LED11
    RETURN

_SELECT3
    CALL LED1
    CALL LED2
    CALL LED3
    CALL LED5
    CALL LED6
    CALL LED7
    CALL LED9
    CALL LED12
    CALL LED11
    CALL LED10
    RETURN

_SELECT5
    CALL LED3
    CALL LED2
    CALL LED1
    CALL LED4
    CALL LED6
    CALL LED7
    CALL LED9
    CALL LED12
    CALL LED11
    CALL LED10
    RETURN

STATE0
    MOVF CNT3_MEM, W
    MOVWF CNT3
    MOVLW CNT5_DEFAULT
    MOVWF CNT5
STATE0_LABEL1
    MOVLW CNT4_DEFAULT
    MOVWF CNT4
    DECFSZ CNT3
    GOTO STATE0_LABEL2
    GOTO STATE1
STATE0_LABEL2
    DECFSZ CNT4
    GOTO _STATE0
    GOTO STATE0_LABEL1
    
STATE1
    MOVF CNT3_MEM, W
    MOVWF CNT3
    MOVLW CNT5_DEFAULT
    MOVWF CNT5
STATE1_LABEL1
    MOVLW CNT4_DEFAULT
    MOVWF CNT4
    DECFSZ CNT3
    GOTO STATE1_LABEL2
    GOTO STATE2
STATE1_LABEL2
    DECFSZ CNT4
    GOTO _STATE1
    GOTO STATE1_LABEL1

STATE2
    MOVF CNT3_MEM, W
    MOVWF CNT3
    MOVLW CNT5_DEFAULT
    MOVWF CNT5
STATE2_LABEL1
    MOVLW CNT4_DEFAULT
    MOVWF CNT4
    DECFSZ CNT3
    GOTO STATE2_LABEL2
    GOTO STATE3
STATE2_LABEL2
    DECFSZ CNT4
    GOTO _STATE2
    GOTO STATE2_LABEL1

STATE3
    MOVF CNT3_MEM, W
    MOVWF CNT3
    MOVLW CNT5_DEFAULT
    MOVWF CNT5
STATE3_LABEL1
    MOVLW CNT4_DEFAULT
    MOVWF CNT4
    DECFSZ CNT3
    GOTO STATE3_LABEL2
    GOTO STATE4
STATE3_LABEL2
    DECFSZ CNT4
    GOTO _STATE3
    GOTO STATE3_LABEL1

STATE4
    MOVF CNT3_MEM, W
    MOVWF CNT3
    MOVLW CNT5_DEFAULT
    MOVWF CNT5
STATE4_LABEL1
    MOVLW CNT4_DEFAULT
    MOVWF CNT4
    DECFSZ CNT3
    GOTO STATE4_LABEL2
    GOTO STATE5
STATE4_LABEL2
    DECFSZ CNT4
    GOTO _STATE4
    GOTO STATE4_LABEL1

STATE5
    MOVF CNT3_MEM, W
    MOVWF CNT3
    MOVLW CNT5_DEFAULT
    MOVWF CNT5
STATE5_LABEL1
    MOVLW CNT4_DEFAULT
    MOVWF CNT4
    DECFSZ CNT3
    GOTO STATE5_LABEL2
    GOTO STATE6
STATE5_LABEL2
    DECFSZ CNT4
    GOTO _STATE5
    GOTO STATE5_LABEL1
    
STATE6
    ; STATE6は砂が全部落ちたアニメーション
    DECFSZ CNT3
    GOTO _STATE6
    ; STATE6終了
    MOVF CNT3_MEM, W
    MOVWF CNT3
    MOVLW CNT5_DEFAULT
    MOVWF CNT5
STATE7
    ; state7は消灯のみ
    DECFSZ CNT3
    GOTO _STATE7
    ; STATE7終了
    GOTO LOOP

_STATE0
    BTFSC PORTB, RESET_SW
    GOTO LOOP
    CALL LED1
    CALL LED2
    CALL LED3
    CALL LED4
    CALL LED5
    CALL LED6
    GOTO STATE0_LABEL2
    
_STATE1
    BTFSC PORTB, RESET_SW
    GOTO LOOP
    MOVF CNT5, W
    SUBLW CNT5_HALF
    BTFSS STATUS, C
    GOTO _STATE1_ON
    ; 点滅
    CALL LED_CLEAR
    CALL LED_CLEAR
    CALL LED_CLEAR
    CALL LED4
    CALL LED5
    CALL LED6
    MOVF CNT5, W
    BTFSS STATUS, Z
    GOTO _STATE1_DEC
    MOVLW CNT5_DEFAULT
    MOVWF CNT5
    GOTO STATE1_LABEL2
_STATE1_ON
    CALL LED1
    CALL LED2
    CALL LED3
    CALL LED4
    CALL LED5
    CALL LED6
    DECF CNT5
    GOTO STATE1_LABEL2
_STATE1_DEC
    DECF CNT5
    GOTO STATE1_LABEL2

_STATE2
    BTFSC PORTB, RESET_SW
    GOTO LOOP
    CALL LED4
    CALL LED5
    CALL LED6
    CALL LED10
    CALL LED11
    CALL LED12
    GOTO STATE2_LABEL2
    
_STATE3
    BTFSC PORTB, RESET_SW
    GOTO LOOP
    MOVF CNT5, W
    SUBLW CNT5_HALF
    BTFSS STATUS, C
    GOTO _STATE3_ON
    ; 点滅
    CALL LED_CLEAR
    CALL LED_CLEAR
    CALL LED6
    CALL LED10
    CALL LED11
    CALL LED12
    MOVF CNT5, W
    BTFSS STATUS, Z
    GOTO _STATE3_DEC
    MOVLW CNT5_DEFAULT
    MOVWF CNT5
    GOTO STATE3_LABEL2
_STATE3_ON
    CALL LED4
    CALL LED5
    CALL LED6
    CALL LED10
    CALL LED11
    CALL LED12
    DECF CNT5
    GOTO STATE3_LABEL2
_STATE3_DEC
    DECF CNT5
    GOTO STATE3_LABEL2

_STATE4
    BTFSC PORTB, RESET_SW
    GOTO LOOP
    CALL LED6
    CALL LED8
    CALL LED9
    CALL LED10
    CALL LED11
    CALL LED12
    GOTO STATE4_LABEL2

_STATE5
