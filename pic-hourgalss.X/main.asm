    ADDLW B'00000111'
    MOVWF PORTB
    MOVLW B'00010000'
    ADDWF PORTA, F
    CALL TIMER_LED
    RETURN
LED7
    MOVLW B'11100011'
    ANDWF PORTA, F
    MOVF PORTB, W
    ANDLW B'11110000'
    ADDLW B'00001110'
    MOVWF PORTB
    MOVLW B'00000100'
    ADDWF PORTA, F
    CALL TIMER_LED
    RETURN
LED8
    MOVLW B'11100011'
    ANDWF PORTA, F
    MOVF PORTB, W
    ANDLW B'11110000'
    ADDLW B'00001101'
