    ANDWF PORTA, F
    MOVF PORTB, W
    ANDLW B'11110000'
    ADDLW B'00000111'
    MOVWF PORTB
    MOVLW B'00000100'
    ADDWF PORTA, F
    CALL TIMER_LED
    RETURN
LED2
