LIST P=PIC16F1827
#include<p16f1827.inc>

LEDH EQU 0x20
LEDL EQU 0x21
SELECT_CNT EQU 0x22

CNT0 EQU 0x30
CNT1 EQU 0x31

BUZZER_CNT1 EQU 0x40

TIMER_CNT0 EQU 0x70
TIMER_CNT1 EQU 0x71
TIMER_CNT2 EQU 0x72


BUZZER EQU D'1'
 
START_SW EQU D'4'
RESET_SW EQU D'5'

__CONFIG    _CONFIG1, _FOSC_INTOSC & _WDTE_OFF & _PWRTE_ON & _MCLRE_OFF & _CP_OFF & _CPD_OFF & _BOREN_OFF & _CLKOUTEN_OFF & _IESO_OFF & _FCMEN_OFF
__CONFIG    _CONFIG2, _WRT_OFF & _PLLEN_OFF & _STVREN_OFF & _BORV_LO & _LVP_OFF

ORG 0x0000
    CLRF PORTA
    CLRF PORTB
    BSF BSR, BSR0
    ; バンク1
    ; クロックを4MHzに設定
    MOVLW B'01101000'
    MOVWF OSCCON
    BSF BSR, BSR1
    ; バンク3
    ; ディジタル入力を受け入れるようにする
    CLRF ANSELB
    ; バンク1
    BSF BSR, BSR0
    BCF BSR, BSR1
    MOVLW 0x00
    MOVWF TRISA
    MOVLW 0x30
    MOVWF TRISB
    ; バンク0
    BCF BSR, BSR0
    CLRF PORTA
    CLRF PORTB
    GOTO LOOP

SELECT_LED_DATA
    ; 1min, 2min
    DT 0x00,0x10,0x00,0x30
    ; 3min, 4min
    DT 0x00,0x70,0x00,0xf0
    ; 5min, 6min
    DT 0x01,0xf0,0x03,0xf0
    ; 7min, 8min
    DT 0x07,0xf0,0x0f,0xf0
    ; 9min, 10min
    DT 0x1f,0xf0,0x3f,0xf0
    ; 11min, 12min
    DT 0x7f,0xf0,0xff,0xf0
LOOP
    MOVLW 0x01
    MOVWF SELECT_CNT
    MOVLW HIGH SELECT_LED_DATA
    MOVWF FSR0H
    MOVLW LOW SELECT_LED_DATA
    MOVWF FSR0L
    BTFSS PORTB, RESET_SW
    GOTO LOOP_L1
    CALL LEDBIT_FSR
    GOTO LOOP
LOOP_L1
    CALL LEDBIT_FSR
    BTFSS PORTB, RESET_SW
    GOTO LOOP_L1
    CALL SELECT_BUZZER
    MOVF SELECT_CNT, W
    SUBLW D'12'
    BTFSC STATUS, Z
    GOTO LOOP
    INCF SELECT_CNT
    ADDFSR FSR0, 0x02
LOOP_L2
    CALL LEDBIT_FSR
    BTFSC PORTB, RESET_SW
    GOTO LOOP_L2
    GOTO LOOP_L1

LEDBIT_FSR
    MOVIW 0[FSR0]
    MOVWF LEDH
    MOVIW 1[FSR0]
    MOVWF LEDL
    CALL LEDBIT
    RETURN
    
LEDBIT
    MOVLW D'9'
    MOVWF CNT0
LED_L1
    DECFSZ CNT0
    GOTO LED_L3
    MOVLW D'5'
    MOVWF CNT0
LED_L2
    DECFSZ CNT0
    GOTO LED_L4
    RETURN

LED_L3
    MOVF CNT0, W
    RLF LEDH, F
    BTFSS STATUS, C
    MOVLW D'0'
    CALL LED_L3_CALC_GOTO
    GOTO LED_L1
LED_L3_CALC_GOTO
    BRW
    GOTO LEDCLR
    GOTO LED5
    GOTO LED6
    GOTO LED7
    GOTO LED8
    GOTO LED9
    GOTO LED10
    GOTO LED11
    GOTO LED12

LED_L4
    MOVF CNT0, W
    RLF LEDL, F
    BTFSS STATUS, C
    MOVLW D'0'
    CALL LED_L4_CALC_GOTO
    GOTO LED_L2
LED_L4_CALC_GOTO
    BRW
    GOTO LEDCLR
    GOTO LED1
    GOTO LED2
    GOTO LED3
    GOTO LED4

SELECT_BUZZER
    MOVLW D'8'
    MOVWF CNT1
_SELECT_BUZZER
    MOVIW 0[FSR0]
    MOVWF LEDH
    MOVIW 1[FSR0]
    MOVWF LEDL
    CALL SELECT_BUZZER_LEDBIT
    DECFSZ CNT1
    GOTO _SELECT_BUZZER
    RETURN
    
SELECT_BUZZER_LEDBIT
    MOVLW D'9'
    MOVWF CNT0
SELECT_BUZZER_L1
    DECFSZ CNT0
    GOTO SELECT_BUZZER_L3
    MOVLW D'5'
    MOVWF CNT0
SELECT_BUZZER_L2
    DECFSZ CNT0
    GOTO SELECT_BUZZER_L4
    RETURN

SELECT_BUZZER_L3
    MOVF CNT0, W
    BTFSS WREG, 0
    GOTO SELECT_BUZZER_L3_OFF
    GOTO SELECT_BUZZER_L3_ON
_SELECT_BUZZER_L3
    RLF LEDH, F
    BTFSS STATUS, C
    MOVLW D'0'
    CALL SELECT_BUZZER_L3_CALC_GOTO
    GOTO SELECT_BUZZER_L1
SELECT_BUZZER_L3_CALC_GOTO
    BRW
    GOTO LEDCLR
    GOTO LED5
    GOTO LED6
    GOTO LED7
    GOTO LED8
    GOTO LED9
    GOTO LED10
    GOTO LED11
    GOTO LED12

SELECT_BUZZER_L3_ON
    BSF PORTA, BUZZER
    GOTO _SELECT_BUZZER_L3
SELECT_BUZZER_L3_OFF
    BCF PORTA, BUZZER
    GOTO _SELECT_BUZZER_L3

SELECT_BUZZER_L4
    MOVF CNT0, W
    BTFSS WREG, 0
    GOTO SELECT_BUZZER_L4_OFF
    GOTO SELECT_BUZZER_L4_ON
_SELECT_BUZZER_L4
    RLF LEDL, F
    BTFSS STATUS, C
    MOVLW D'0'
    CALL SELECT_BUZZER_L4_CALC_GOTO
    GOTO SELECT_BUZZER_L2
SELECT_BUZZER_L4_CALC_GOTO
    BRW
    GOTO LEDCLR
    GOTO LED1
    GOTO LED2
    GOTO LED3
    GOTO LED4

SELECT_BUZZER_L4_ON
    BSF PORTA, BUZZER
    GOTO _SELECT_BUZZER_L4
SELECT_BUZZER_L4_OFF
    BCF PORTA, BUZZER
    GOTO _SELECT_BUZZER_L4

LEDCLR
    MOVLW B'11100011'
    ANDWF PORTA, F
    MOVF PORTB, W
    ANDLW B'11110000'
    ADDLW 0x00
    MOVWF PORTB
    MOVLW 0x00
    ADDWF PORTA, F
    CALL TIMER_LED
    RETURN

LED1
