;1ms
TIMER_LED
    MOVLW D'199'
    MOVWF TIMER_CNT1
TIMER_LED_L1
    NOP
    NOP
    DECFSZ TIMER_CNT1,F
    GOTO TIMER_LED_L1
    RETURN

TIMER_1000MS
    MOVLW D'173'
    MOVWF TIMER_CNT1
TIMER_1000MS_L1
    MOVLW D'191'
    MOVWF TIMER_CNT2
TIMER_1000MS_L2
    DECFSZ TIMER_CNT2,F
    GOTO TIMER_1000MS_L2
    NOP
    DECFSZ TIMER_CNT1,F
    GOTO TIMER_1000MS_L1
    NOP
    RETURN

