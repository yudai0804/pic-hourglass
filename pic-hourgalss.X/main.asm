LIST P=PIC16F1827
#include<p16f1827.inc>

CNT0	EQU	0x70
CNT1	EQU	0x71
CNT2	EQU	0x72
CNT3 EQU 0x73
CNT4 EQU 0x74
CNT4_DEFAULT EQU d'200'
CNT4_HALF EQU d'100'
TMP EQU 0x74
__CONFIG	_CONFIG1, _FOSC_INTOSC & _WDTE_OFF & _PWRTE_ON & _MCLRE_OFF & _CP_OFF & _CPD_OFF & _BOREN_OFF & _CLKOUTEN_OFF & _IESO_OFF & _FCMEN_OFF
__CONFIG	_CONFIG2, _WRT_OFF & _PLLEN_OFF & _STVREN_OFF & _BORV_LO & _LVP_OFF

ORG 0x0000
    CLRF PORTA
    CLRF PORTB
    BSF BSR, BSR0
    BSF BSR, BSR1
    ; バンク3
    ; ディジタル入力を受け入れるようにする
    ; CLRF ANSELAをすると何故かRA4の動きがXORみたいな動きをするようになる。
    ; 今回はANSELAを行わないことで対応
    CLRF ANSELB
    ; バンク1
    BSF BSR, BSR0
    BCF BSR, BSR1
    MOVLW 0x00
    MOVWF TRISA
    MOVLW 0x30
    MOVWF TRISB
    ; バンク0
    BCF BSR, BSR0
    CLRF PORTA
    CLRF PORTB
LOOP
    MOVLW 0xff
    MOVWF CNT3
    MOVLW CNT4_DEFAULT
    MOVWF CNT4
STATE0
    DECFSZ CNT3
    GOTO _STATE0
    MOVLW CNT4_DEFAULT
    MOVWF CNT4
    GOTO LOOP
STATE1
    DECFSZ CNT3
    GOTO _STATE1
    MOVLW CNT4_DEFAULT
    MOVWF CNT4
STATE2
    DECFSZ CNT3
    GOTO _STATE2
    MOVLW CNT4_DEFAULT
    MOVWF CNT4
STATE3
    DECFSZ CNT3
    GOTO _STATE3
    MOVLW CNT4_DEFAULT
    MOVWF CNT4
STATE4
    DECFSZ CNT3
    GOTO _STATE4
    GOTO LOOP

_STATE0
    GOTO _STATE0_ON
    MOVF CNT4, W
    SUBLW CNT4_HALF
    BTFSS STATUS, C
    ; 点滅
    CALL D4
    CALL TIMER_LED
    CALL D5
    CALL TIMER_LED
    CALL D6
    CALL TIMER_LED
    CALL D4
    CALL TIMER_LED
    CALL D5
    CALL TIMER_LED
    CALL D6
    CALL TIMER_LED
    MOVF CNT4, W
    BTFSS STATUS, Z
    GOTO _STATE0_DEC
    MOVLW CNT4_DEFAULT
    MOVWF CNT4
    GOTO STATE0
_STATE0_ON
    CALL D1
    CALL TIMER_LED
    CALL D2
    CALL TIMER_LED
    CALL D3
    CALL TIMER_LED
    CALL D4
    CALL TIMER_LED
    CALL D5
    CALL TIMER_LED
    CALL D6
    CALL TIMER_LED
    DECF CNT4
    GOTO STATE0
_STATE0_DEC
    DECF CNT4
    GOTO STATE0

_STATE1
    CALL D4
    CALL TIMER_LED
    CALL D5
    CALL TIMER_LED
    CALL D6
    CALL TIMER_LED
    CALL D10
    CALL TIMER_LED
    CALL D11
    CALL TIMER_LED
    CALL D12
    CALL TIMER_LED
    GOTO STATE1

_STATE2
    MOVF CNT4, W
    SUBLW CNT4_HALF
    BTFSS STATUS, C
    GOTO _STATE2_ON
    ; 点滅
    CALL CLEAR
    CALL D6
    CALL TIMER_LED
    CALL D6
    CALL TIMER_LED
    CALL D6
    CALL TIMER_LED
    CALL D6
    CALL TIMER_LED
    CALL D6
    CALL TIMER_LED
    CALL D6
    CALL TIMER_LED
    MOVF CNT4, W
    BTFSS STATUS, Z
    GOTO _STATE2_DEC
    MOVLW CNT4_DEFAULT
    MOVWF CNT4
    GOTO STATE2
_STATE2_ON
    CALL D4
    CALL TIMER_LED
    CALL D5
    CALL TIMER_LED
    CALL D6
    CALL TIMER_LED
    CALL D10
    CALL TIMER_LED
    CALL D11
    CALL TIMER_LED
    CALL D12
    CALL TIMER_LED
    DECF CNT4
    GOTO STATE2
_STATE2_DEC
    DECF CNT4
    GOTO STATE2

_STATE3
    CALL D6
    CALL TIMER_LED
    CALL D8
    CALL TIMER_LED
    CALL D9
    CALL TIMER_LED
    CALL D10
    CALL TIMER_LED
    CALL D11
    CALL TIMER_LED
    CALL D12
    CALL TIMER_LED
    GOTO STATE3

_STATE4
    MOVF CNT4, W
    SUBLW CNT4_HALF
    BTFSS STATUS, C
    GOTO _STATE4_ON
    ; 点滅
    CALL CLEAR
    CALL TIMER_LED
    CALL TIMER_LED
    CALL TIMER_LED
    CALL TIMER_LED
    CALL TIMER_LED
    CALL TIMER_LED
    MOVF CNT4, W
    BTFSS STATUS, Z
    GOTO _STATE4_DEC
    MOVLW CNT4_DEFAULT
    MOVWF CNT4
    GOTO STATE4
_STATE4_ON
    CALL D6
    CALL TIMER_LED
    CALL D8
    CALL TIMER_LED
    CALL D9
    CALL TIMER_LED
    CALL D10
    CALL TIMER_LED
    CALL D11
    CALL TIMER_LED
    CALL D12
    CALL TIMER_LED
    DECF CNT4
    GOTO STATE4
_STATE4_DEC
    DECF CNT4
    GOTO STATE4

CLEAR
    MOVF PORTA, W
    ANDLW 0b11100011
    ADDLW 0x00
    MOVWF PORTA
    MOVF PORTB, W
    ANDLW 0x11110000
    ADDLW 0x00
    MOVWF PORTB
    RETURN

