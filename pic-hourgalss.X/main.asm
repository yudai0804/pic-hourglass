;100us
TIMER_LED
	MOVLW	D'19'
	MOVWF	CNT1
TIMER_LED_LABEL1
	NOP
	NOP
	DECFSZ	CNT1,F
	GOTO	TIMER_LED_LABEL1
	RETURN

TIMER_10MS
	MOVLW	D'19'
	MOVWF	CNT1
TIMER_10MS_LABEL1
	MOVLW	D'174'
	MOVWF	CNT2
TIMER_10MS_LABEL2
	DECFSZ	CNT2,F
	GOTO	TIMER_10MS_LABEL2
	DECFSZ	CNT1,F
	GOTO	TIMER_10MS_LABEL1
	NOP
	RETURN

    
TIMER_1000MS
