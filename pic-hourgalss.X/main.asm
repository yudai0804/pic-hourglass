;1ms
TIMER_LED
	MOVLW	D'199'
	MOVWF	CNT1
TIMER_LED_LABEL1
	NOP
	NOP
	DECFSZ	CNT1,F
	GOTO	TIMER_LED_LABEL1
	RETURN

TIMER_10MS
	MOVLW	D'19'
	MOVWF	CNT1
