    CALL LED8
    CALL LED9
    CALL LED10
    CALL LED11
    CALL LED12
    MOVF CNT5, W
    BTFSS STATUS, Z
    GOTO _STATE5_DEC
    MOVLW CNT5_DEFAULT
    MOVWF CNT5
    GOTO STATE5_LABEL2
_STATE5_ON
    CALL LED6
    CALL LED8
    CALL LED9
    CALL LED10
    CALL LED11
    CALL LED12
    DECF CNT5
    GOTO STATE5_LABEL2
_STATE5_DEC
    DECF CNT5
    GOTO STATE5_LABEL2

_STATE6
    BTFSC PORTB, RESET_SW
    GOTO LOOP
    CALL LED7
    CALL LED8
    CALL LED9
    CALL LED10
    CALL LED11
    CALL LED12
    GOTO STATE6

_STATE7
    BTFSC PORTB, RESET_SW
    GOTO LOOP
    CALL LED_CLEAR
    CALL LED_CLEAR
    CALL LED_CLEAR
    CALL LED_CLEAR
    CALL LED_CLEAR
    CALL LED_CLEAR
    GOTO STATE7

LED_CLEAR
    MOVF PORTA, W
    ANDLW 0b11100011
    ADDLW 0x00
    MOVWF PORTA
    MOVF PORTB, W
    ANDLW 0x11110000
    ADDLW 0x00
    MOVWF PORTB
    CALL TIMER_LED
    RETURN

LED1
